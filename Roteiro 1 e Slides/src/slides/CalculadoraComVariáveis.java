package slides;

public class CalculadoraComVariáveis {

	public static void main(String[] args) {
		//Imprimindo mensagem de Boas Vindas
		System.out.println("Bem Vindo(a) a Calculadora de Médias!!");
		double nota1 = 9.0, nota2 = 5.8, nota3 = 6.3, media;
		//Somando as três notas e imprimindo a respectiva média
		media = ((nota1 + nota2 + nota3) /3);
		System.out.println("Nota1: 9.0    Nota2: 5.8   Nota3: 6.3");
		System.out.println("Media: " + media);

	}

}
